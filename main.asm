;                       Asm calculator
;                      =================
; Calculator accepts three args. Second arg should be one of the valid
; operators: + - / *
; As the result of division calculator gives number with remainder.
;
; Examples: 
;   ./asmcalc 1 + 1
;   ./asmcalc 2 * 2
;   ./asmcalc 4 - 1
